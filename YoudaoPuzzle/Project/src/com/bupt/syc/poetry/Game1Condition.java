package com.bupt.syc.poetry;

public class Game1Condition {
	int level;
	int amount;
	
	
	public Game1Condition() {
		super();
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	public Game1Condition(int level, int amount) {
		super();
		this.level = level;
		this.amount = amount;
	}
	
	
}
